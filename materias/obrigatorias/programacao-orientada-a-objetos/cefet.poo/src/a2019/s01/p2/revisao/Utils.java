package a2019.s01.p2.revisao;

public class Utils {
	
	public static double calculaMedia( double a1, double a2) {
		return (a1+a2)/2;
	}
	
	public static double calculaMedia( double a1, double a2, double a3) {
		return (a1+a2+a3)/3;
	}
	
	public static double calculaMedia( double a1, double a2, double a3, double a4) {
		return (a1+a2+a3+a4)/4;
	}
}

